let temperature = 36;

if (temperature > 30) {
  console.log("Вы можете надеть легкую одежду.");
} else if (temperature > 20) {
  console.log("Нужно надеть теплую одежду.");
} else if (temperature < 10) {
  console.log("Сегодня холодно, нужно надеть зимнюю одежду.");
} else {
  console.log("Температура находится в норме.");
}
